//while loop
let count = 0;
while (count < 5) {
  console.log("Count is: " + count);
  count++;
} // Output: Count is: 0
  //         Count is: 1
  //         Count is: 2
  //         Count is: 3
  //         Count is: 4

//do...while loop
let number = 0;         
do {
  console.log("Number is: " + number);
  number++;
} while (number < 5); // Output: Number is: 0
                      //         Number is: 1
                      //         Number is: 2
                      //         Number is: 3
                      //         Number is: 4


